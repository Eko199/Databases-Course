USE ships

SELECT DISTINCT SHIP
FROM OUTCOMES
WHERE SHIP LIKE 'C%' OR SHIP LIKE 'K%'

SELECT DISTINCT COUNTRY
FROM CLASSES c
JOIN SHIPS s ON c.CLASS = s.CLASS
JOIN OUTCOMES o ON NAME = SHIP
WHERE RESULT = 'sunk'

SELECT DISTINCT COUNTRY
FROM CLASSES c
WHERE COUNTRY NOT IN 
	(SELECT DISTINCT COUNTRY
	FROM CLASSES c
	JOIN SHIPS s ON c.CLASS = s.CLASS
	JOIN OUTCOMES o ON NAME = SHIP
	WHERE RESULT = 'sunk')

SELECT c.CLASS
FROM CLASSES c
LEFT JOIN SHIPS s ON s.CLASS = c.CLASS AND LAUNCHED > 1921
WHERE LAUNCHED IS NULL

SELECT CLASS, COUNTRY, BORE * 2.54 AS BORE
FROM CLASSES
WHERE NUMGUNS IN (6, 8, 10)

SELECT DISTINCT c1.COUNTRY
FROM CLASSES c1
JOIN CLASSES c2 ON c1.COUNTRY = c2.COUNTRY
WHERE c1.BORE < c2.BORE

SELECT DISTINCT COUNTRY
FROM CLASSES
WHERE NUMGUNS >= ALL(SELECT NUMGUNS FROM CLASSES)

