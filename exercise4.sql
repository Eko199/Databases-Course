USE ships

SELECT NAME, COUNTRY, NUMGUNS, LAUNCHED
FROM SHIPS s
JOIN CLASSES c ON s.CLASS = c.CLASS

SELECT DISTINCT SHIP
FROM OUTCOMES
JOIN BATTLES ON BATTLE = NAME
WHERE YEAR(DATE) = 1942

