SELECT *
FROM PC
JOIN PRODUCT p ON PC.MODEL = p.MODEL
WHERE PRICE <= ALL(
	(SELECT PRICE 
	FROM LAPTOP l 
	JOIN PRODUCT pr ON l.MODEL = pr.MODEL 
	WHERE pr.MAKER = p.MAKER)
	UNION
	(SELECT PRICE 
	FROM PRINTER 
	JOIN PRODUCT ON PRINTER.MODEL = PRODUCT.MODEL 
	WHERE PRODUCT.MAKER = p.MAKER)
)
