-- Problem 1
USE movies

SELECT DISTINCT STUDIONAME, STARNAME
FROM MOVIE
JOIN STARSIN ON TITLE = MOVIETITLE AND YEAR = MOVIEYEAR
ORDER BY STUDIONAME

SELECT DISTINCT NAME
FROM MOVIEEXEC
JOIN MOVIE ON CERT# = PRODUCERC#
JOIN STARSIN ON TITLE = MOVIETITLE AND YEAR = MOVIEYEAR
WHERE STARNAME = 'Harrison Ford'

SELECT NAME
FROM MOVIESTAR
JOIN STARSIN ON NAME = STARNAME
JOIN MOVIE ON TITLE = MOVIETITLE AND YEAR = MOVIEYEAR
WHERE STUDIONAME = 'MGM' AND GENDER = 'F'

SELECT NAME, m2.TITLE
FROM MOVIEEXEC
JOIN MOVIE m1 ON m1.PRODUCERC# = CERT#
JOIN MOVIE m2 ON m1.PRODUCERC# = CERT#
WHERE m1.TITLE = 'Star Wars'

SELECT TITLE, YEAR, NAME
FROM MOVIE
LEFT JOIN MOVIEEXEC ON PRODUCERC# = CERT#

SELECT TITLE, YEAR, me.NAME, STUDIONAME, s.ADDRESS
FROM MOVIE
LEFT JOIN MOVIEEXEC me ON PRODUCERC# = CERT#
LEFT JOIN STUDIO s ON STUDIONAME = s.NAME

SELECT NAME
FROM MOVIESTAR
LEFT JOIN STARSIN ON STARNAME = NAME
WHERE MOVIETITLE IS NULL

-- Problem 2
USE pc

SELECT DISTINCT p.MODEL, PRICE
FROM PRODUCT p
LEFT JOIN PC ON p.MODEL = PC.MODEL
WHERE TYPE = 'PC'

SELECT MAKER, p.MODEL, p.TYPE
FROM PRODUCT p
LEFT JOIN PC ON p.MODEL = PC.MODEL
LEFT JOIN LAPTOP l ON p.MODEL = l.MODEL
LEFT JOIN PRINTER pr ON p.MODEL = pr.MODEL
WHERE PC.CODE IS NULL AND l.CODE IS NULL AND pr.CODE IS NULL

