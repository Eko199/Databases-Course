SELECT BATTLE
FROM OUTCOMES
JOIN SHIPS s ON SHIP = NAME
JOIN CLASSES c ON c.CLASS = s.CLASS
WHERE NUMGUNS < 9
GROUP BY BATTLE
HAVING COUNT(SHIP) >= 3
	AND SUM(CASE WHEN RESULT = 'ok' THEN 1 ELSE 0 END) >= 2

