SELECT maker
FROM product p
LEFT JOIN pc ON p.model = pc.model
LEFT JOIN laptop l ON p.model = l.model
GROUP BY maker
HAVING AVG(pc.price) < AVG(l.price)

SELECT p.model
FROM product p
JOIN pc ON p.model = pc.model
GROUP BY p.model, p.maker
HAVING AVG(price) < (SELECT MIN(price) 
					FROM product p2 
					JOIN laptop l ON p2.model = l.model 
					WHERE p2.maker = p.maker)

