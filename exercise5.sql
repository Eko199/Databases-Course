FROM SHIPS s
JOIN CLASSES c ON s.CLASS = c.CLASS

SELECT c.CLASS, MIN(LAUNCHED) AS [First Year], MAX(LAUNCHED) AS [Last Year]
FROM CLASSES c
LEFT JOIN SHIPS s ON c.CLASS = s.CLASS
GROUP BY c.CLASS

SELECT c.CLASS, SUM(CASE RESULT WHEN 'sunk' THEN 1 ELSE 0 END) AS [Sunk Ships]
FROM CLASSES c
LEFT JOIN SHIPS s ON c.CLASS = s.CLASS
LEFT JOIN OUTCOMES o ON NAME = SHIP
GROUP BY c.CLASS

