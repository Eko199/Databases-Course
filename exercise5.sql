-- Problem 1
USE pc

SELECT AVG(SPEED) AS [Average Speed]
FROM PC

SELECT MAKER, AVG(SCREEN) AS [Average Laptop Screen]
FROM PRODUCT p
LEFT JOIN LAPTOP l ON p.MODEL = l.MODEL
GROUP BY MAKER

SELECT AVG(SPEED) AS [Average Speed]
FROM LAPTOP
WHERE PRICE > 1000

SELECT AVG(PRICE) AS [Average Price]
FROM PC
JOIN PRODUCT p ON PC.MODEL = p.MODEL
WHERE MAKER = 'A'

SELECT AVG(PRICE) AS [Average Price]
FROM (
	(SELECT PRICE, MODEL FROM PC) 
	UNION 
	(SELECT PRICE, MODEL FROM LAPTOP)
) c
JOIN PRODUCT p ON c.MODEL = p.MODEL
WHERE MAKER = 'B'

SELECT SPEED, AVG(PRICE) AS [Average Price]
FROM PC
GROUP BY SPEED

SELECT MAKER
FROM PRODUCT p
JOIN PC ON p.MODEL = PC.MODEL
GROUP BY MAKER
HAVING COUNT(DISTINCT p.MODEL) >= 3

SELECT DISTINCT MAKER
FROM PRODUCT p
JOIN PC ON p.MODEL = PC.MODEL
WHERE PRICE = (SELECT MAX(PRICE) FROM PC)

SELECT SPEED, AVG(PRICE) AS [Average Price]
FROM PC
GROUP BY SPEED
HAVING SPEED > 800

SELECT AVG(HD) AS [Average Storage]
FROM PC
JOIN PRODUCT p ON PC.MODEL = p.MODEL
WHERE MAKER IN (SELECT MAKER FROM PRODUCT WHERE TYPE = 'Printer')

SELECT MAX(PRICE) - MIN(PRICE) AS [Price Difference]
FROM LAPTOP
GROUP BY SCREEN

